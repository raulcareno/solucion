package utilerias;

/**
 *
 * @author Geovanny
 */
public class NotasIngresar {
    public Integer matricula;
    public Integer id;
    public Double nota;
    public Double desde;
    public Double hasta;
    public Boolean estado;
    public String nombre;
    public Boolean texto;
    public String color;
    public String colorEstado;

    public NotasIngresar() {
    }

    public Integer getMatricula() {
        return matricula;
    }

    public void setMatricula(Integer matricula) {
        this.matricula = matricula;
    }

    public Double getNota() {
        return nota;
    }

    public void setNota(Double nota) {
        this.nota = nota;
    }

    public Double getDesde() {
        return desde;
    }

    public void setDesde(Double desde) {
        this.desde = desde;
    }

    public Double getHasta() {
        return hasta;
    }

    public void setHasta(Double hasta) {
        this.hasta = hasta;
    }

    public Boolean getEstado() {
        return estado;
    }

    public void setEstado(Boolean estado) {
        this.estado = estado;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Boolean getTexto() {
        return texto;
    }

    public void setTexto(Boolean texto) {
        this.texto = texto;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getColorEstado() {
        return colorEstado;
    }

    public void setColorEstado(String colorEstado) {
        this.colorEstado = colorEstado;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
    
    
    
}