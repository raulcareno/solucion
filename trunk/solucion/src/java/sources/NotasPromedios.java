public class NotasPromedios {

    public NotasPromedios() {
    }
    String titulo;
    String anioBasica;
    String anioLectivo;
    String subtitulo;
    Object cantidad;
    String rango;
    public String getAnioBasica() {
        return anioBasica;
    }

    public void setAnioBasica(String anioBasica) {
        this.anioBasica = anioBasica;
    }

    public String getAnioLectivo() {
        return anioLectivo;
    }

    public void setAnioLectivo(String anioLectivo) {
        this.anioLectivo = anioLectivo;
    }

    public Object getCantidad() {
        return cantidad;
    }

    public void setCantidad(Object cantidad) {
        this.cantidad = cantidad;
    }

    public String getSubtitulo() {
        return subtitulo;
    }

    public void setSubtitulo(String subtitulo) {
        this.subtitulo = subtitulo;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getRango() {
        return rango;
    }

    public void setRango(String rango) {
        this.rango = rango;
    }
      
}
