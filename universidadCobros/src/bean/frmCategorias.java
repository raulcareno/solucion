    public void listar() {

        List lista = adm.query("Select o from CategoriasSociales as o ");
        DefaultTableModel dtm = (DefaultTableModel) tableCategoriasSociales.getModel();
        dtm.getDataVector().removeAllElements();
        for (Iterator it = lista.iterator(); it.hasNext();) {
            CategoriasSociales elem = (CategoriasSociales) it.next();
            Object obj[] = new Object[7];
            obj[0] = elem.getIdCategoriasSociales();
            obj[1] = elem.getNombre();
            obj[2] = elem.getValorCredito();
            dtm.addRow(obj);
        }
        tableCategoriasSociales.setModel(dtm);
    }
